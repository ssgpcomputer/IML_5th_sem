import numpy as np 

a = [10,24,35,49]
b = [1,2,3,4]

a = np.array(a)
b = np.array(b)

add_ans = np.add(a, b)
print(add_ans)
print("\n\n")

sub_ans = np.subtract(a, b)
print(sub_ans)
print("\n\n")

mul_ans = np.multiply(a, b)
print(mul_ans)
print("\n\n")

div_ans = np.divide(a, b)
print(div_ans)
print("\n\n")
